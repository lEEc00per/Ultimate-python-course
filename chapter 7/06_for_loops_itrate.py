# For loops with lists

l = [1, 4, 6, 234, 6, 764]  #'l' for list

for i in l:
    print(i)

print("------------------------------------------------------")

#For loops with tuples

t = (6, 231, 75, 122)  #'t' for tuple
for i in t:
    print(i)

print("------------------------------------------------------")

#For loops with strings

s = "Jerry"  #'s' for string
for i in s:
    print(i)